#include "3-calc.h"

/**
 * op_add - adds two numbers
 * @a: first integer
 * @b: second integer
 * Return: result
 */
int op_add(int a, int b)
{
return (a + b);
}

/**
 * op_sub - substracts one number from the other
 * @a: first integer
 * @b: second integer
 * Return: result
 */
int op_sub(int a, int b)
{
return (a - b);
}
/**
 * op_mul - multiplies two numbers
 * @a: first integer
 * @b: second integer
 * Return: result
 */
int op_mul(int a, int b)
{
return (a * b);
}

/**
 * op_div - divides one number by the other
 * @a: first integer
 * @b: second integer
 * Return: result
 */
int op_div(int a, int b)
{
return (a / b);
}

/**
 * op_mod - performs modulo
 * @a: first integer
 * @b: second integer
 * Return: result
 */
int op_mod(int a, int b)
{
return (a % b);
}
